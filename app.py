import streamlit as st
import pandas as pd
import joblib

# Load model and scaler
model = joblib.load("final_model.pkl")
scaler = joblib.load("scaler.pkl")

st.title("ðŸŒ¿ Green Space & Mental Wellbeing Predictor")

# User inputs
ndvi = st.slider("NDVI Score", 0.0, 1.0, 0.7)
tree_density = st.slider("Tree Density", 0, 500, 300)
area = st.number_input("Green Space Area (sq. meters)", 1000, 10000, 5000)
paths = st.slider("Walking Paths", 0, 10, 5)
seats = st.slider("Seating Areas", 0, 20, 10)

# Prepare input
input_df = pd.DataFrame([[ndvi, tree_density, area, paths, seats]],
                        columns=["NDVI_Score", "Tree_Density", "Area_sq_meters", "Walking_Paths", "Seating_Areas"])

# Predict
scaled_input = scaler.transform(input_df)
prediction = model.predict(scaled_input)

st.success(f"ðŸ§  Predicted Mental Wellbeing Score: {round(prediction[0], 2)}")
